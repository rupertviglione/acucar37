const elements = {
  year: document.querySelector("#current-year"),
  langButtons: document.querySelectorAll(".lang-btn"),
  menuToggle: document.querySelector(".menu-toggle"),
  nav: document.querySelector(".main-nav"),
  contactForm: document.querySelector("#contact-form")
};

function setMeta(name, content, attribute = "name") {
  const tag = document.querySelector(`meta[${attribute}="${name}"]`);
  if (tag) tag.setAttribute("content", content);
}

function applyTranslation(node, value, allowHtml = false) {
  if (!value) return;
  if (allowHtml && /<\/?[a-z][^>]*>/i.test(value)) {
    node.innerHTML = value;
  } else {
    node.textContent = value;
  }
}

function setLanguage(language) {
  const dictionary = translations[language] || translations.pt;
  document.documentElement.lang = language === "en" ? "en" : "pt-PT";
  document.title = dictionary.pageTitle;

  setMeta("description", dictionary.metaDescription);
  setMeta("og:title", dictionary.pageTitle, "property");
  setMeta("og:description", dictionary.ogDescription, "property");
  setMeta("og:locale", language === "en" ? "en_US" : "pt_PT", "property");
  setMeta("twitter:title", dictionary.pageTitle);
  setMeta("twitter:description", dictionary.ogDescription);

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (dictionary[key] !== undefined) {
      // Allow HTML for the verification steps (which include <strong>)
      const allowHtml = key.startsWith("verifyStep");
      applyTranslation(node, dictionary[key], allowHtml);
    }
  });

  document.querySelectorAll("[data-i18n-aria-label]").forEach((node) => {
    const key = node.getAttribute("data-i18n-aria-label");
    if (dictionary[key]) node.setAttribute("aria-label", dictionary[key]);
  });

  elements.langButtons.forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  try { localStorage.setItem("acucar37-language", language); } catch (_) { /* private mode */ }
}

function closeMenu() {
  if (!elements.nav || !elements.menuToggle) return;
  elements.nav.classList.remove("is-open");
  elements.menuToggle.setAttribute("aria-expanded", "false");
  document.body.classList.remove("nav-open");
}

function initNavigation() {
  if (!elements.menuToggle || !elements.nav) return;
  elements.menuToggle.addEventListener("click", () => {
    const isOpen = elements.nav.classList.toggle("is-open");
    elements.menuToggle.setAttribute("aria-expanded", String(isOpen));
    document.body.classList.toggle("nav-open", isOpen);
  });
  elements.nav.querySelectorAll("a").forEach((link) => link.addEventListener("click", closeMenu));
}

function initLanguageSwitcher() {
  elements.langButtons.forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.dataset.lang));
  });

  let storedLanguage = null;
  try { storedLanguage = localStorage.getItem("acucar37-language"); } catch (_) { /* private mode */ }
  const urlLanguage = new URLSearchParams(window.location.search).get("lang");
  const initialLanguage = urlLanguage === "en" || storedLanguage === "en" ? "en" : "pt";
  setLanguage(initialLanguage);
}

function initYear() {
  if (elements.year) elements.year.textContent = new Date().getFullYear();
}

function initContactForm() {
  if (!elements.contactForm) return;
  elements.contactForm.addEventListener("submit", (event) => {
    event.preventDefault();
    const data = new FormData(elements.contactForm);
    const name = (data.get("name") || "").toString().trim();
    const email = (data.get("email") || "").toString().trim();
    const subject = (data.get("subject") || "Assunto geral").toString().trim();
    const message = (data.get("message") || "").toString().trim();

    if (!name || !email || !message) {
      elements.contactForm.reportValidity();
      return;
    }

    const body = `${message}\n\n— \n${name}\n${email}`;
    const mailto = `mailto:geral@acucar37.pt?subject=${encodeURIComponent("[Site] " + subject)}&body=${encodeURIComponent(body)}`;
    window.location.href = mailto;
  });
}

initYear();
initNavigation();
initLanguageSwitcher();
initContactForm();
